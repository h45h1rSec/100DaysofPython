#Operators

# Addition -> It adds two values. => 8+9
# Subtraction -> It subtracts two values. => 8-9
# Multiplication -> It multiplies two values. => 8*9
# Division -> It divdes two values. => 8/9
# Modulus -> It returns the remainder of two divided values. => 8%9
# FLoor Division -> It divdes two values, but returns it without decimals. => 8//9
# Exponet -> It exponents two values. => 8**9, i.e. 8^9

print(20+5)
print(20-5)
print(20*5)
print(20/5)
print(20%5)
print(20//5)
print(20**5)

